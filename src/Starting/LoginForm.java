    private void methodlogin() {
        PreparedStatement ps;
        ResultSet rs;
        String nama = inputusername.getText();
        String pass = String.valueOf((inputpassword.getPassword()));

        if (nama.equals("") && pass.equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Field Terlebih Dahulu");
        } else if (nama.equals("")) {
            JOptionPane.showMessageDialog(this, "Field Nama Masih Kosong");
        } else if (pass.equals("")) {
            JOptionPane.showMessageDialog(this, "Field Password Masih Kosong");
        } else {
            String query = "SELECT * FROM `petugas` WHERE `username` =? AND`password` =?";

            try {
                ps = koneksidb.getConnection().prepareStatement(query);
                ps.setString(1, nama);
                ps.setString(2, pass);
                rs = ps.executeQuery();

                if (rs.next()) {
                    landingmenu lm = new landingmenu(rs.getString("noktp"));
                    lm.setVisible(true);
                    lm.pack();
                    lm.setLocationRelativeTo(null);
                    lm.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);

                    dialog_loginberhasil y = new dialog_loginberhasil(rs.getString("nama_petugas"), rs.getString("username"));
                    y.setVisible(true);
                    y.pack();
                    y.setLocationRelativeTo(null);
                    y.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();

                } else {
                    dialog_logingagal gagal = new dialog_logingagal();
                    gagal.setVisible(true);
                    gagal.pack();
                    gagal.setLocationRelativeTo(null);
                    gagal.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);

                }
            } catch (SQLException ex) {
                dialog_logingagaldb gagal = new dialog_logingagaldb();
                gagal.setVisible(true);
                gagal.pack();
                gagal.setLocationRelativeTo(null);
                gagal.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                System.out.println(ex);
            }
        }
    }
